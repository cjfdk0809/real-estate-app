def find_lawd_code(query):
    """주소 또는 시군구명으로 LAWD_CD를 검색."""
    q = query.strip().replace('  ', ' ')
    if q in LAWD_CODES:
        return LAWD_CODES[q]
    # 부분 매칭
    for name, code in LAWD_CODES.items():
        if q in name or name in q:
            return code
    return None
